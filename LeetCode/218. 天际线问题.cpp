class Solution {
public:
    vector<vector<int>> getSkyline(vector<vector<int>>& buildings) {
        vector<pair<int, int>> bs;
        // 
        for(auto b : buildings){
            bs.push_back({b[0], -b[2]}); // 起点，负数表示建筑物的高度
            bs.push_back({b[1], b[2]});
        }
        sort(bs.begin(), bs.end());
        for(auto b : bs){
            cout << b.first << ' ' << b.second << endl;
        }

        unordered_map<int, int> ht;
        vector<vector<int>> res;
        priority_queue<int> pq;
        int prev_h = 0;
        pq.push(0);
        for(auto b : bs){
            int x = b.first;
            int h = b.second;

            if(h < 0){
                pq.push(-h);
            }
            else{
                ht[h]++;
            }

            while(!pq.empty() && ht[pq.top()] > 0){
                ht[pq.top()]--;
                pq.pop();
            }
            int cur_h = pq.top();
            if(cur_h != prev_h){
                prev_h = cur_h;
                res.push_back({x, cur_h});
            }
        }
        return res;
    }
};

// 我们用 pair<int, int> 的默认排序逻辑（先比第一个元素，第一个相同则比第二个）来拆解为什么要把左边界设为负数。
// 1. 核心排序规则对比在 std::sort 中，对于 pair<int, int>：如果 x1 < x2，则 (x1, y1) 排在前面。如果 x1 == x2，则比较 y1 和 y2，小的排在前面。
// 这就是我们要利用的特性。
// 2. 场景一：两个建筑在同一位置开始 (左边界重合)假设 $x=10$ 处有两个建筑：高度分别为 20 和 50。如果你存正数：{10, 20} 和 {10, 50}。排序结果：{10, 20} 先被处理。过程：高度先跳到 20（记录一次转折），紧接着跳到 50（又记录一次）。错误：多了一个 [10, 20] 的点。如果你存负数 (-h)：{10, -20} 和 {10, -50}。排序结果：{10, -50} 先被处理（因为 -50 < -20）。过程：高度直接从 0 跳到 50。正确：只记录 [10, 50]。
// 3. 场景二：建筑 A 结束的同时，建筑 B 开始 (一左一右重合)这是最容易出错的地方。假设 $x=10$ 时，高度为 20 的 A 结束，高度为 50 的 B 开始。如果你都存正数：{10, 20} (结束) 和 {10, 50} (开始)。排序结果：{10, 20} (结束) 先处理。过程：高度先掉到 0（记录 [10, 0]），然后立刻跳到 50。错误：天际线在大楼交界处掉到了地上。如果你用 -h (左) 和 +h (右)：左边界（开始）：{10, -50}右边界（结束）：{10, 20}排序结果：{10, -50} 先处理（因为负数一定比正数小）。过程：先加入 50，此时堆顶是 50。然后处理 20 的结束（标记 20 待删），堆顶依然是 50。正确：高度平滑过渡，没有多余掉落。4. 场景三：两个建筑在同一位置结束 (右边界重合)假设 $x=10$ 时，高度为 20 和 50 的建筑同时结束。你的代码逻辑：存入的是 +h（右边界）。存入：{10, 20} 和 {10, 50}。排序结果：{10, 20} 先处理（因为 20 < 50）。过程：先标记 20 待删，堆顶依然是 50（无转折）。然后处理 50 结束，堆顶降落。正确：只在高度最终下落时记录一个点。💡 记忆口诀我们可以把 pair 的第二个参数看作 “优先级”。数值越小，优先级越高（越先被处理）：左边界 (负数) VS 右边界 (正数)：负数胜出。 $\rightarrow$ 先上后下。左 VS 左 (负数)：绝对值大的（更小的负数）胜出。 $\rightarrow$ 高者领先。右 VS 右 (正数)：数值小的胜出。 
